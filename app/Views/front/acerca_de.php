                    <h3>Aeropuerto Internacional Doctor Fernando Piragine Niveyro</h3>
                    <p>
                    

                      El Aeropuerto Internacional Doctor Fernando Piragine Niveyro, también conocido como Cambá Punta, es el principal aeropuerto de la provincia de Corrientes, Argentina. Se encuentra ubicado a 10 kilómetros al este de la capital provincial, en la localidad de Cambá Punta.
                      <br>
                      <h4>Historia</h4>
                      
                      El aeropuerto fue inaugurado en 1946, bajo el nombre de Aeropuerto Cambá Punta. En 1954 fue rebautizado como Aeropuerto Internacional Doctor Fernando Piragine Niveyro, en honor al médico y político correntino Fernando Piragine Niveyro.
                      <br>
                      A lo largo de su historia, el aeropuerto ha sido testigo del desarrollo de la provincia de Corrientes. En sus inicios, el aeropuerto operaba vuelos nacionales de cabotaje. En la década de 1990, con la apertura de la economía argentina, el aeropuerto comenzó a recibir vuelos internacionales.
                      <br><br>
                      <h4>Valores</h4>
                      
                      El Aeropuerto Internacional Doctor Fernando Piragine Niveyro se compromete con los siguientes valores:
                      <br>
                      
                        <li> <b>Seguridad:</b> El aeropuerto se compromete a brindar un entorno seguro para sus pasajeros y empleados.</li>
                        <li><b>Calidad:</b> El aeropuerto se compromete a brindar un servicio de calidad a sus pasajeros y clientes.</li>
                        <li><b>Sostenibilidad:</b> El aeropuerto se compromete a operar de manera sostenible, minimizando su impacto ambiental.</li>
                        <br>
                      
                      
                      <h4>Proyecciones a futuro</h4>

                      <p> El Aeropuerto Internacional Doctor Fernando Piragine Niveyro tiene como objetivo convertirse en un centro de conectividad regional. Para ello, el aeropuerto está invirtiendo en la ampliación de su infraestructura y en la mejora de sus servicios.</p>

                      
                      <p> Entre los proyectos de ampliación del aeropuerto se encuentran la construcción de una nueva pista de aterrizaje, la ampliación del edificio terminal y la ampliacion de la sala de embarque.</p>

                      
                      <p> El aeropuerto también está mejorando sus servicios para brindar una mejor experiencia a sus pasajeros. Estos servicios incluyen la ampliación de las áreas comerciales, la mejora de las instalaciones de seguridad y la modernización de los sistemas de información.</p>

                      
                      <p> Con estas inversiones, el Aeropuerto Internacional Doctor Fernando Piragine Niveyro se posiciona como un importante hub aéreo para la región.  </p>

